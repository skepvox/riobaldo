<script setup lang="ts">
import { formatDateByLocale } from '~/utils'
import type { HomeSection } from '~/types/home'

const props = defineProps<{
  section: HomeSection
}>()

const { data: posts } = await useAsyncData('index-blogs', () =>
  queryCollection('blog').order('date', 'DESC').limit(3).all()
)
if (!posts.value) {
  throw createError({ statusCode: 404, statusMessage: 'Posts do blog nÃ£o encontrados', fatal: true })
}

const blogPosts = computed(() => (posts.value ?? []).map((post, index) => ({
  ...post,
  image: post.image
    ? {
        src: post.image,
        width: index === 0 ? 672 : 437,
        height: index === 0 ? 378 : 246,
        alt: `${post.title} image`
      }
    : undefined
})))
</script>

<template>
  <UPageSection
    :title="props.section.title"
    :ui="{
      container: 'px-4 sm:px-6 lg:px-8 !pt-0 sm:gap-6 lg:gap-8',
      title: 'text-left text-xl sm:text-xl lg:text-2xl font-medium',
      description: 'text-left mt-2 text-sm sm:text-md lg:text-sm text-muted'
    }"
  >
    <template #description>
      <WorkInProgress />
    </template>
    <UBlogPosts
      orientation="vertical"
      class="gap-4 lg:gap-y-4"
    >
      <UBlogPost
        v-for="(post, index) in blogPosts"
        :key="post.path ?? index"
        orientation="horizontal"
        variant="naked"
        v-bind="post"
        :to="post.path"
        :ui="{
          root: 'group relative lg:items-start lg:flex ring-0 hover:ring-0',
          body: '!px-0',
          header: 'hidden'
        }"
      >
        <template #date>
          {{ formatDateByLocale('pt-BR', post.date) }}
        </template>
        <template #footer>
          <UButton
            size="xs"
            variant="link"
            class="px-0 gap-0"
            label="Ler artigo"
          >
            <template #trailing>
              <UIcon
                name="i-lucide-arrow-right"
                class="size-4 text-primary transition-all opacity-0 group-hover:translate-x-1 group-hover:opacity-100"
              />
            </template>
          </UButton>
        </template>
      </UBlogPost>
    </UBlogPosts>
  </UPageSection>
</template>
